export function getUniqueMedicationNames(): string[] {
  const uniqueNames = new Set<string>()
  allMedications.forEach((med) => uniqueNames.add(med.name))
  return Array.from(uniqueNames).sort()
}

export function searchMedications(query: string): Medication[] {
  if (query.length < 3) {
    return []
  }

  const lowerQuery = query.toLowerCase()
  const seen = new Set<string>()
  const results: Medication[] = []

  for (const med of allMedications) {
    if (seen.has(med.name)) continue

    // Match if medication name starts with the query
    if (med.name.toLowerCase().startsWith(lowerQuery)) {
      results.push(med)
      seen.add(med.name)
    }
  }

  return results.slice(0, 50) // Limit to 50 results for performance
}

export function getMedicationById(id: string): Medication | null {
  return allMedications.find((med) => med.id === id) || null
}

export function getMedicationsByName(name: string): Medication[] {
  return allMedications.filter((med) => med.name === name)
}
